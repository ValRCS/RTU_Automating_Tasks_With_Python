# Let us print some basic arithmetic operations
# I can print just result
print(2 + 3)
# Or I can print with explanation
print("Addition: 2 + 3 =", 2 + 3)
print("Subtraction: 5 - 2 =", 5 - 2)
print("Multiplication: 3 * 4 =", 3 * 4)
print("Division: 10 / 2 =", 10 / 2)

# let us look at division witout remainder
print("Integer Division: 10 // 4 =", 10 // 4) # this will be 2 not 2.5 !! integer division
# also modulus operator to get remainder
print("Modulus: 10 % 3 =", 10 % 3)
# and exponentiation
print("Exponentiation: 2 ** 3 =", 2 ** 3) # so two cubed is 8
# let's make a Googol
print("A Googol is: 10**100 =", 10**100) # Python offers big integers too automatically